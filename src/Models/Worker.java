
package Models;

public class Worker {
    private int workerID;
    private String workerFirstName;
    private String workerLastName;
    private String workerAdress;
    private String workerPhone;
    private int workerSalary;
    private String workerDate;

    public Worker() {
    }

    public int getWorkerID() {
        return workerID;
    }

    public void setWorkerID(int workerID) {
        this.workerID = workerID;
    }

    public String getWorkerFirstName() {
        return workerFirstName;
    }

    public void setWorkerFirstName(String workerFirstName) {
        this.workerFirstName = workerFirstName;
    }

    public String getWorkerLastName() {
        return workerLastName;
    }

    public void setWorkerLastName(String workerLastName) {
        this.workerLastName = workerLastName;
    }

    public String getWorkerAdress() {
        return workerAdress;
    }

    public void setWorkerAdress(String workerAdress) {
        this.workerAdress = workerAdress;
    }

    public String getWorkerPhone() {
        return workerPhone;
    }

    public void setWorkerPhone(String workerPhone) {
        this.workerPhone = workerPhone;
    }

    public int getWorkerSalary() {
        return workerSalary;
    }

    public void setWorkerSalary(int workerSalary) {
        this.workerSalary = workerSalary;
    }

    public String getWorkerDate() {
        return workerDate;
    }

    public void setWorkerDate(String workerDate) {
        this.workerDate = workerDate;
    }
    
    
}
